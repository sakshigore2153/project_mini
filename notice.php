  <li><em><strong>The students are advised to keep themselves aware of the Institute rules. They are also advised to go through detailed Hostel rules and Prevention of Ragging rules given separately. Ignorance of rules shall not be an excuse for violation.</strong></em></li>
  <li><em><strong>College working hours are 10.30 A.M. to 4.45 P.M. with a tiffin break between 1.30 P.M. to 1.45 P.M.</strong></em></li>
  <li><em><strong>As per Vidyasagar University regulations, 75% attendance is compulsory. Otherwise, students cannot appear for the University Examinations.</strong></em></li>
  <li><em><strong>Absence from class must be justified with the concerned Department. If a student is absent from class/classes without notices and/or absent without proper reasons for more than 5 days his/her name may be struck of college roll.</strong></em></li>
  <li><em><strong>Absence for more than three days for reasons of health requires a medical certificate. Producing a medical certificate however does not entitle a student to get attendance.</strong></em></li>
  <li><em><strong>&nbsp;Participation in co-curricular or extra-curricular activities inside or outside the campus is not counted as absence from class. Attendance will be counted only when a copy of the participation certificate or a note from the concerned teacher is produced before the Head of the Department.</strong></em></li>
  <li><em><strong>The students are required to wear college Identity Card on all college working days. Failure to wear the college identity card will be treated as misconduct and such students will be barred from entering the campus and class room.</strong></em></li>
  <li><em><strong>The college takes note of serious misbehaviour, insubordination, habitual tardiness, irregular work habits or obscenity which are punishable by fine, suspension or dismissal.</strong></em></li>
  <li><em><strong>The College is declared an alcohol-smoke-drug-free area. Any student found smoking or under the influence of intoxication of alcohol/drugs in the Institute or in the Hostel is liable to strict disciplinary action which may be up to expulsion from the Institute.</strong></em></li>
  <li><em><strong>The students are warned they must follow all the safety regulations while conducting practical in the laboratories and the management is not responsible for any physical damage or mishap that might occur out of students&rsquo; negligence.</strong></em></li>
  <li><em><strong>&nbsp;Internal assessment is part of the evaluation. Two internal examinations are conducted for B.A. / B.Sc. (Hons.) students and one internal examination is conducted for B.A. / B.Sc. (General) students.&nbsp; Absence in such tests, even with medical certificate, cannot entitle a student for a re-test.</strong></em></li>
  <li><em><strong>Students&rsquo; vehicles may be parked and locked in the allotted place only. However, the College is not responsible for their safety.</strong></em></li>
  <li><em><strong>The management is not responsible for the safety of valuables of students.</strong></em></li>
  <li><em><strong>Change of permanent address must be notified to the College office.</strong></em></li>
  <li><em><strong>Students who do not live with their parents or in the College Hostel are required to inform the Principal about the details of their place of residence.&nbsp; It is compulsory for the students to attend functions /activities organized by the institute on various occasions like Independence Day, Republic Day, Annual Day, etc. whether the function falls on a working day or on holidays. Absence from such functions without valid reasons will invite disciplinary action.</strong></em></li>
